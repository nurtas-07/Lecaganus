export function t(language, path) {
  return path.split(".").reduce((value, key) => value?.[key], UI[language]) ?? path;
}

export function optionLabel(value, language) {
  return OPTION_LABELS[language]?.[value] ?? value;
}

export function textFor(item, field, language) {
  const value = item[field];
  if (value && typeof value === "object") return value[language] || value.RU || value.EN || "";
  return CONTENT_TRANSLATIONS[item.id]?.[language]?.[field] || value;
}
